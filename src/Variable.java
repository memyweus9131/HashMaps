public class Variable {
    public static void main(String[] args) {

        int a = 5;
        System.out.println (a);

        short b = 12;
        System.out.println(b);

        long c = 123456789;
        System.out.println(c);

        float d = 1234567;
        System.out.println(d);

        double e = 1234567;
        System.out.println (e);

        char f = 'M';
        System.out.println(f);

        String g = "salary";
        System.out.println(g);

    }
}
